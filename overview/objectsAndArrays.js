//////// Objects work well when: ////////

// 1. Order isn't necessary 
// 2. When you need fast insertion + removal

//////// Big O of  Objects ////////

// Insertion O(1)
// Removal O(1)
// Search O(n)
// Access O(1)

//////// Big O of Object Methods ////////

// Object.keys O(n)
// Object.values O(n)
// Object.entries O(n)
// hasOwnProperty O(1)

//////// Arrays work well when: ////////

// 1. Order is necessary 
// 2. When you need fast insertion + removal (sort of...)

//////// Big O of Arrays ////////

// Insertion - Depends if beginning or end of array due to reindexing necessary for beginning
// Removal - Depends if beginning or end of array due to reindexing necessary for beginning
// Search O(n)
// Access O(1)
 
//////// Big O of Array Methods ////////

// Array.unshift() O(n)
// Array.push() O(1)
// Array.shift() O(n)
// Array.concat() O(n)
// Array.slice() O(n)
// Array.splice() O(n)
// Array.sort() O(nlogn)
// Array.forEach/map/filter/etc() O(n)




